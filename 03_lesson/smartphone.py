# Класс Smartphone хранит данные о телефоне:
# марка (бренд), модель, и номер
class Smartphone:
    def __init__(self, brand, model, number):
        self.brand = brand    # Марка телефона (например, Apple)
        self.model = model    # Модель телефона (например, iPhone 13)
        self.number = number  # Номер телефона (например, +79161234567)
